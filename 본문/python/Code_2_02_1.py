# 사칙 연산
print(869 + 120) # 989
print(869 - 120) # 749
print(869 * 120) # 104280
print(869 / 120) # 7.241666666666666
print(869 // 120) # 7

# 나머지(mod)
print(8 % 5) # 3
print(869 % 120) # 29

# 절댓값(abs)
print(abs(-45)) # 45
print(abs(15)) # 15

# 제곱
print(10 ** 2) # 100
print(3 ** 4) # 81

# 루트(sqrt)
import math
print("{:.5f}".format(math.sqrt(4.0))) # 2.00000
print("{:.5f}".format(math.sqrt(2.0))) # 1.41421
